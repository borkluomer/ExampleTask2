package javaInterviewQuestion;

public class SumNumbers {
    public static void main(String[] args) {

        //handle with numbers

        int number [] = {1,2,3,4};
        int sum = 0;

        for (int i = 0 ; i<number.length; i++){
            sum = sum + number[i];

        }
        System.out.println(sum + " is total balance");

    }




}
